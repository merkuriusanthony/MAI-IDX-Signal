def get_sector(symbol: str) -> str:
    return IDX_SECTORS.get(symbol.upper().replace('.JK', ''), {}).get('sector', 'Unknown')


def get_industry(symbol: str) -> str:
    return IDX_SECTORS.get(symbol.upper().replace('.JK', ''), {}).get('industry', '')


def get_profile(symbol: str) -> dict:
    from app.data.boards import get_board

    d = dict(IDX_SECTORS.get(
        symbol.upper().replace('.JK', ''),
        {'sector': 'Unknown', 'industry': '', 'name': ''},
    ))
    d["board"] = get_board(symbol)
    return d